package conditionals;

public class IfElseStatement {

    public static void main(String[] args) {

        /**
         * the else after the if only runs if the if condition
         * is false
         */
        if (10 > 3) {
            System.out.println("10 is greater than 3");
        }else{
            System.out.println("10 is not greater than 3");
        }
        
        // an else must always have an if statement

    }

}
