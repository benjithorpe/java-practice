package loops;

public class ForLoop {
    public static void main(String[] args) {
        // for loop template and structure
        // for(initialization; condition; iteration){
        //      block of code
        // }
        //
        // the iteration can be an increment or decrement
        // use for loop if you know how many times the loop should run
        
        
        // counting from 0 to 4
        for(int i = 0; i < 5; i++){
            System.out.println("I is " + i);
        }
    }
    
}
